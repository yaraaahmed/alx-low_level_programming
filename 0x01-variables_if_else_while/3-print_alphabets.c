#include <stdio.h>
/**
 * main - Entry point
 * description:program that prints the alphabet in lowercase
 * Return:always 0 (sucsses)
*/

int main(void)
{
	char ch = 'a';
	char CH = 'A';

	while (ch <= 'z')
	{
		putchar(ch);
		ch++;
	}

	while (CH <= 'Z')
	{
		putchar(CH);
		CH++;
	}
	putchar('\n');

	return (0);
}
